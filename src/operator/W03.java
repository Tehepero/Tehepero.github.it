package operator;

public class W03 {
    public static void main(String[] args) {

        System.out.println(10 < 9);
        System.out.println(10 > 9); // 10 이 9보다 큰게 맞으니까 true 가 나온게 맞음
        System.out.println(10 >= 9); // 10 이 9보다 크거나 같음? 맞음
        System.out.println(10 < 9); // 10이 9보다 크니? 아니 X 틀림
        System.out.println(10 <= 9); // 10이 9보다 크거나 같니? 아니 X 틀림
        System.out.println(10 == 10); // 10이 10이랑 같니? 맞음
        System.out.println(10 == 9); // 10이 9랑 같니? X 틀림
        System.out.println(10 != 10); // 10은 10이 아니니? 틀림 10이 맞음
        System.out.println(10 != 9); // 10은 9가 아니니? 맞음 9가 아님 True






    }
}
